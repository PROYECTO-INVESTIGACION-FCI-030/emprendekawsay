UPDATE public.roles_usuario ru
SET id_rol = r.id
FROM public.roles r
WHERE ru.id_rol IS NULL
  AND ru.rol = r.codigo;

DO $$
BEGIN
  IF EXISTS (
    SELECT 1
    FROM information_schema.columns
    WHERE table_schema = 'public'
      AND table_name = 'perfiles_usuario'
      AND column_name = 'rol'
  ) THEN
    EXECUTE $sql$
      INSERT INTO public.roles_usuario (id_usuario, id_rol, rol, fecha_asignacion)
      SELECT p.id, r.id, p.rol, now()
      FROM public.perfiles_usuario p
      JOIN public.roles r ON r.codigo = p.rol
      WHERE p.rol IS NOT NULL
        AND NOT EXISTS (
          SELECT 1 FROM public.roles_usuario ru WHERE ru.id_usuario = p.id
        )
    $sql$;
  END IF;
END $$;

