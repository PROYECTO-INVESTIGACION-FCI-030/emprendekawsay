-- Politicas por bloque: heredan permisos desde la encuesta cabecera.
DO $$
DECLARE
  t TEXT;
BEGIN
  FOREACH t IN ARRAY ARRAY[
    'encuesta_consentimiento',
    'encuesta_datos_sociodemograficos',
    'encuesta_informacion_emprendimiento',
    'encuesta_gestion_empresarial',
    'encuesta_finanzas',
    'encuesta_marketing_ventas',
    'encuesta_tecnologia_digitalizacion',
    'encuesta_capital_social_redes',
    'encuesta_aspectos_personales_familiares',
    'encuesta_barreras_oportunidades'
  ]
  LOOP
    EXECUTE format('DROP POLICY IF EXISTS %I ON public.%I', t || '_select', t);
    EXECUTE format('DROP POLICY IF EXISTS %I ON public.%I', t || '_write', t);
    EXECUTE format(
      'CREATE POLICY %I ON public.%I FOR SELECT USING (
        EXISTS (
          SELECT 1 FROM public.encuestas_iniciales e
          WHERE e.id = id_encuesta
            AND (e.id_participante = auth.uid() OR public.get_my_rol() IN (''administradora'', ''investigadora''))
        )
      )',
      t || '_select',
      t
    );
    EXECUTE format(
      'CREATE POLICY %I ON public.%I FOR ALL USING (
        EXISTS (
          SELECT 1 FROM public.encuestas_iniciales e
          WHERE e.id = id_encuesta
            AND (e.id_participante = auth.uid() OR public.get_my_rol() = ''administradora'')
        )
      ) WITH CHECK (
        EXISTS (
          SELECT 1 FROM public.encuestas_iniciales e
          WHERE e.id = id_encuesta
            AND (e.id_participante = auth.uid() OR public.get_my_rol() = ''administradora'')
        )
      )',
      t || '_write',
      t
    );
  END LOOP;
END $$;
