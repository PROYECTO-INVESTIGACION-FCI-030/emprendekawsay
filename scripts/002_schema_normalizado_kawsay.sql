CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  rol_inicial public.tipo_rol;
  rol_id BIGINT;
BEGIN
  rol_inicial := COALESCE((NEW.raw_user_meta_data ->> 'rol_asignado')::public.tipo_rol, 'mujer_emprendedora');

  INSERT INTO public.perfiles_usuario (id, nombre_completo, email)
  VALUES (
    NEW.id,
    NULLIF(NEW.raw_user_meta_data ->> 'nombre_completo', ''),
    NEW.email
  )
  ON CONFLICT (id) DO UPDATE
  SET email = EXCLUDED.email,
      nombre_completo = COALESCE(public.perfiles_usuario.nombre_completo, EXCLUDED.nombre_completo),
      fecha_actualizacion = now();

  SELECT id INTO rol_id FROM public.roles WHERE codigo = rol_inicial LIMIT 1;

  INSERT INTO public.roles_usuario (id_usuario, id_rol, rol)
  VALUES (NEW.id, rol_id, rol_inicial)
  ON CONFLICT (id_usuario) DO UPDATE
  SET id_rol = EXCLUDED.id_rol,
      rol = EXCLUDED.rol,
      fecha_asignacion = now();

  RETURN NEW;
END;
$$;

