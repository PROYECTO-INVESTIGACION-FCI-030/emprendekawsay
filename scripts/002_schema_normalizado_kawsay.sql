LEFT JOIN public.roles r ON r.id = ru.id_rol;

CREATE OR REPLACE FUNCTION public.get_my_rol()
RETURNS public.tipo_rol
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT r.codigo
  FROM public.perfiles_usuario p
  LEFT JOIN public.roles_usuario ru ON ru.id_usuario = p.id
  LEFT JOIN public.roles r ON r.id = ru.id_rol
  WHERE p.id = auth.uid()
  LIMIT 1;
$$;

